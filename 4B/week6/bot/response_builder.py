def handle_faq_query(user_message: str, entities: Dict) -> str:
    results = search_kb(user_message, top_k=3)

    if results:
        context_parts = []
        for r in results:
            if r["type"] == "faq":
                faq = r["payload"]
                context_parts.append(
                    f"Q: {faq.get('question', '')}\nA: {faq.get('answer', '')}"
                )
        if context_parts:
            context = "\n\n".join(context_parts)
            generated = _generate_with_llm(user_message, context)
            if generated:
                return generated

        # Fallback: return the raw top answer
        top = results[0]
        if top["type"] == "faq":
            faq = top["payload"]
            response = f"{faq.get('answer', '')}\n\n"
            if len(results) > 1:
                response += "Related:\n"
                for r in results[1:3]:
                    if r["type"] == "faq":
                        response += f"• {r['payload'].get('question', '')}\n"
            return response

    return (
        "I don't have specific information about that. "
        "Please contact the temple directly at (469) 606-3119 or visit jkyog.org."
    )


def handle_event_query(user_message: str, entities: Dict) -> str:
    results = search_kb(user_message, top_k=3)
    event_results = [r for r in results if r["type"] == "event"]

    if event_results:
        context_parts = []
        for r in event_results:
            e = r["payload"]
            parts = [f"Event: {e.get('title', '')}"]
            if e.get("description"):
                parts.append(f"Description: {e['description']}")
            if e.get("day"):
                timing = e["day"]
                if e.get("start_time"):
                    timing += f" at {e['start_time']}"
                if e.get("end_time"):
                    timing += f" - {e['end_time']}"
                parts.append(f"When: {timing}")
            if e.get("start_day"):
                parts.append(f"Dates: {e['start_day']} to {e.get('end_day', '')}")
            if e.get("location"):
                parts.append(f"Location: {e['location']}")
            context_parts.append("\n".join(parts))

        context = "\n\n".join(context_parts)
        generated = _generate_with_llm(user_message, context)
        if generated:
            return generated

        # Fallback: format event list
        response = "Events:\n\n"
        for r in event_results[:3]:
            e = r["payload"]
            response += f"• {e.get('title', 'Event')}\n"
            if e.get("day"):
                response += f"  {e['day']}"
                if e.get("start_time"):
                    response += f" at {e['start_time']}"
                response += "\n"
        return response

    # Try calendar integration (primary via CalendarWrapper)
    date_filter = entities.get("date") if isinstance(entities, dict) else None
    calendar_events = _get_calendar_events_from_wrapper(date_filter)

    # Robust fallback: if wrapper fails/unavailable, use legacy integration.
    if not calendar_events:
        calendar_events = (
            get_events_on_date(entities["date"])
            if "date" in entities
            else get_upcoming_events(limit=5)
        )
    if calendar_events:
        response = "Upcoming Events:\n\n"
        for event in calendar_events[:3]:
            response += f"• {event.get('summary', 'Event')}\n"
            response += f"  {event.get('start', '')}\n"
        return response

    return (
        "I couldn't find specific event information. "
        "Please check our website at jkyog.org or call (469) 606-3119."
    )


