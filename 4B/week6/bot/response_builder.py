def _generate_with_gemini(user_message: str, context: str) -> str | None:
    """
    Generate a conversational response using Gemini with the given context.
    Legacy path used when single-call is disabled or fails.
    Returns None if Gemini is unavailable or errors.
    """
    if not _GOOGLE_AVAILABLE or not os.getenv("GOOGLE_API_KEY"):
        return None
    try:
        client = genai.Client(api_key=os.getenv("GOOGLE_API_KEY"))
        prompt = f"Context:\n{context}\n\nUser message: {user_message}"
        response = client.models.generate_content(
            model=os.getenv("GEMINI_MODEL_RESPONSE", "gemini-2.5-flash"),
            contents=prompt,
            config=genai_types.GenerateContentConfig(
                system_instruction=_BOT_SYSTEM_PROMPT,
                temperature=0.4,
                max_output_tokens=600,
            ),
        )
        return response.text.strip()
    except Exception as exc:
        logger.warning("Gemini response generation error: %s", exc)
        return None


def _build_mixed_context(user_message: str) -> str:
    """
    Build a rich context block for the unified single-call path.
    Combines top KB matches (FAQs + events) with static temple info and donation link
    so Gemini can choose the best answer in one call.
    """
    parts: list[str] = []

    results = search_kb(user_message, top_k=5)
    faq_parts: list[str] = []
    event_parts: list[str] = []
    for r in results:
        if r["type"] == "faq" and len(faq_parts) < 2:
            faq = r["payload"]
            faq_parts.append(f"Q: {faq.get('question', '')}\nA: {faq.get('answer', '')}")
        elif r["type"] == "event" and len(event_parts) < 2:
            e = r["payload"]
            event_line = f"Event: {e.get('title', '')}"
            if e.get("day"):
                event_line += f" | When: {e['day']}"
                if e.get("start_time"):
                    event_line += f" at {e['start_time']}"
            event_parts.append(event_line)

    if faq_parts:
        parts.append("FAQs:\n" + "\n\n".join(faq_parts))
    if event_parts:
        parts.append("Events:\n" + "\n".join(event_parts))

    parts.append(
        f"Temple: {TEMPLE_NAME}\n"
        f"Address: {TEMPLE_ADDRESS}\n"
        "Phone: (469) 606-3119\n"
        "Website: jkyog.org"
    )

    try:
        donation_link = get_donation_link(temple_slug="dallas")
        parts.append(f"Donation link: {donation_link}")
    except Exception:
        pass

    return "\n\n---\n\n".join(parts)


def _single_call_gemini(user_message: str, context: str) -> dict | None:
    """
    Single Gemini call that classifies intent and generates the reply together.
    Returns {"intent": str, "confidence": float, "reply_text": str, "next_action": str|None}
    or None on any failure so the caller can fall back to the legacy pipeline.
    """
    if not _GOOGLE_AVAILABLE or not os.getenv("GOOGLE_API_KEY"):
        return None
    try:
        client = genai.Client(api_key=os.getenv("GOOGLE_API_KEY"))
        prompt = f"Context:\n{context}\n\nUser message: {user_message}"
        response = client.models.generate_content(
            model=os.getenv("GEMINI_MODEL_RESPONSE", "gemini-2.5-flash"),
            contents=prompt,
            config=genai_types.GenerateContentConfig(
                system_instruction=_UNIFIED_SYSTEM_PROMPT,
                temperature=0.4,
                max_output_tokens=700,
            ),
        )
        raw = response.text.strip()
        # Strip markdown code fences the model may add despite instructions
        raw = re.sub(r"^```(?:json)?\s*|\s*```$", "", raw, flags=re.MULTILINE).strip()
        result = json.loads(raw)
        return {
            "intent": result.get("intent", "unknown"),
            "confidence": float(result.get("confidence", 0.0)),
            "reply_text": (result.get("reply_text") or "").strip(),
            "next_action": result.get("next_action"),
        }
    except Exception as exc:
        logger.warning("Single-call Gemini failed, falling back to legacy: %s", exc)
        return None


def _generate_with_grok(user_message: str, context: str) -> str | None:
    """
    Generate a conversational response using xAI Grok via the /v1/responses endpoint.
    Returns None if unavailable or errors.

    Endpoint: https://api.x.ai/v1/responses
    Ref: https://docs.x.ai/developers/quickstart
    """
    if not _HTTPX_AVAILABLE:
        return None

    api_key = os.getenv("XAI_API_KEY")
    if not api_key:
        return None

    base_url = os.getenv("XAI_BASE_URL", "https://api.x.ai/v1/responses").strip()
    model = os.getenv("XAI_MODEL", "grok-3-mini").strip()
    timeout_s = float(os.getenv("LLM_TIMEOUT_S", "15"))

    prompt = f"Context:\n{context}\n\nUser message: {user_message}"

    headers = {
        "Authorization": f"Bearer {api_key}",
        "Content-Type": "application/json",
    }
    payload = {
        "model": model,
        "input": [
            {"role": "system", "content": _BOT_SYSTEM_PROMPT},
            {"role": "user", "content": prompt},
        ],
        "temperature": 0.4,
    }

    try:
        with httpx.Client(timeout=timeout_s) as client:
            r = client.post(base_url, headers=headers, json=payload)
            r.raise_for_status()
            data = r.json()
        return (data.get("output_text", "") or "").strip() or None
    except Exception as exc:
        print(f"Grok response generation error: {exc}")
        return None


def _generate_with_llm(user_message: str, context: str) -> str | None:
    """
    Provider switch for response generation.

    Env:
      - LLM_PROVIDER: 'gemini' (default) or 'grok'
    """
    provider = (os.getenv("LLM_PROVIDER", "gemini") or "gemini").strip().lower()
    if provider == "grok":
        return _generate_with_grok(user_message, context)
    return _generate_with_gemini(user_message, context)


def _strip_html(html: str) -> str:
    # Google Directions step instructions include HTML tags.
    return re.sub(r"<[^>]+>", "", html or "")


def _get_directions_text_from_maps_wrapper(user_location: str) -> str:
    """
    Primary directions path:
      user_location -> MapsWrapper().directions(...) -> format into directions_text.
    """
    if not MapsWrapper:
        return ""

    try:
        print("calling MapsWrapper.directions (render log)")
        logger.info("calling MapsWrapper.directions | origin=%s", user_location)

        wrapper = MapsWrapper()
        directions_result = wrapper.directions(
            origin=user_location,
            destination=TEMPLE_ADDRESS,
            mode="driving",
        )

        routes = directions_result.get("routes") or []
        if not routes:
            return "❌ Could not find directions for that route."

        legs = routes[0].get("legs") or []
        if not legs:
            return "❌ Could not find directions for that route."

        leg0 = legs[0]
        steps = leg0.get("steps") or []
        total_distance = (leg0.get("distance") or {}).get("text", "") or ""
        total_duration = (leg0.get("duration") or {}).get("text", "") or ""

        lines = []
        if total_distance or total_duration:
            lines.append(f"🗺️ {total_distance} • {total_duration}".strip())

        for idx, step in enumerate(steps, start=1):
            instruction = _strip_html(step.get("html_instructions", "") or "")
            distance = (step.get("distance") or {}).get("text", "") or ""
            if instruction and distance:
                lines.append(f"{idx}. {instruction} ({distance})")
            elif instruction:
                lines.append(f"{idx}. {instruction}")

        return "\n".join(lines).strip() or "❌ Could not find directions for that route."
    except CircuitBreakerOpenError:
        logger.warning("MapsWrapper directions circuit breaker open")
        return ""
    except Exception as exc:
        logger.exception("MapsWrapper directions failed")
        return ""


def _date_part(iso_or_datetime_str: str) -> str:
    """
    Extract 'YYYY-MM-DD' from Google Calendar start values:
      - 'YYYY-MM-DD' (all-day)
      - 'YYYY-MM-DDTHH:MM:SS...' (dateTime)
    """
    if not iso_or_datetime_str:
        return ""
    if len(iso_or_datetime_str) >= 10:
        return iso_or_datetime_str[:10]
    return ""


def _get_calendar_events_from_wrapper(date_str: Optional[str]) -> list[Dict]:
    """
    Primary events path:
      CalendarWrapper().list_events(max_results=20) -> simplify -> optional date filter.
    """
    if not CalendarWrapper:
        return []

    try:
        print("calling CalendarWrapper.list_events (render log)")
        logger.info("calling CalendarWrapper.list_events | date_filter=%s", date_str)

        wrapper = CalendarWrapper()
        calendar_result = wrapper.list_events(max_results=20)
        raw_events = calendar_result.get("events") or []

        simplified: list[Dict] = []
        for item in raw_events:
            summary = item.get("summary") or "Event"
            start_obj = item.get("start") or {}
            start_val = start_obj.get("dateTime") or start_obj.get("date") or ""
            simplified.append({"summary": summary, "start": start_val})

        # If a date was requested, filter by matching the date part of start.
        if date_str:
            simplified = [
                e for e in simplified
                if _date_part(e.get("start", "")).strip() == date_str
            ]

        # WhatsApp-friendly output: only show the most relevant few.
        if not date_str:
            simplified = simplified[:5]

        return simplified
    except CircuitBreakerOpenError:
        logger.warning("CalendarWrapper list_events circuit breaker open")
        return []
    except Exception as exc:
        logger.exception("CalendarWrapper list_events failed")
        return []


